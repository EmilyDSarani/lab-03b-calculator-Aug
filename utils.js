export function add(dope1, dope2) {
    return Number(dope1) + Number(dope2);
}
export function subtract(dope1, dope2) {
    return Number(dope1) - Number(dope2);
}
export function multiply(dope1, dope2) {
    return Number(dope1) * Number(dope2);
}
export function divide(dope1, dope2) {
    return Number(dope1) / Number(dope2);
}